.include "my-macro"	# подключение файла с макроопределениями

.text			# программа высчитывания факториала числа
        
.global _start		# точка входа
_start:		
        movl $5, %eax	# число факториала - 5
	movl $1, %ebx	# начальное число - 1

L1: 			# цикл с известным числом повторений
        cmpl  $0, %eax	# проверка, если предыдущие условие выполняется, переход на следующий шаг
	je    L2	
	imull %eax, %ebx	# умножает число из %eax на число из %ebx
	decl  %eax		# уменьшает число из %eax на единицу
	jmp   L1		# переход в начало цикла L1
	
L2:			# выход из цикла, если условие не выполняется
        Finish		# функций Finish из файла my-macro
.end  			# конец

